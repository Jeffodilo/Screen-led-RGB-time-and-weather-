void draw_weather_icon(int x, int y, uint16_t *weather, int imageHeight, int imageWidth)
{

  //int imageHeight = 13;
  //int imageWidth = 13;

  int counter = 0;
  for (int yy = 0; yy < imageHeight; yy++)
  {
    for (int xx = 0; xx < imageWidth; xx++)
    {

      display.drawPixel(xx + x , yy + y, weather[counter]);
      counter++;

    }
  }
}

